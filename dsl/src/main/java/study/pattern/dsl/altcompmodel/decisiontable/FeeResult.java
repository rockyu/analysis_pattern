package study.pattern.dsl.altcompmodel.decisiontable;

public class FeeResult {

    private int     fee;

    private boolean shouldAlertRepresentative;

    public FeeResult(int fee, boolean shouldAlertRepresentative) {
        this.fee = fee;
        this.shouldAlertRepresentative = shouldAlertRepresentative;
    }

    public int getFee() {
        return fee;
    }

    public boolean isShouldAlertRepresentative() {
        return shouldAlertRepresentative;
    }

}
